package com.ISCAS.OneBeltOneRoad.entity.br;

public class BrAnnotationData {
    private Integer id;
    private String name;
    private Object layerFeatures;
    private Object time;
    private Object style;
    private Object chart;
    private Object keyPoint;
    private Object layers;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Object getLayerFeatures() {
        return layerFeatures;
    }

    public void setLayerFeatures(Object layerFeatures) {
        this.layerFeatures = layerFeatures;
    }

    public Object getTime() {
        return time;
    }

    public void setTime(Object time) {
        this.time = time;
    }

    public Object getStyle() {
        return style;
    }

    public void setStyle(Object style) {
        this.style = style;
    }

    public Object getChart() {
        return chart;
    }

    public void setChart(Object chart) {
        this.chart = chart;
    }

    public Object getKeyPoint() {
        return keyPoint;
    }

    public void setKeyPoint(Object keyPoint) {
        this.keyPoint = keyPoint;
    }

    public Object getLayers() {
        return layers;
    }

    public void setLayers(Object layers) {
        this.layers = layers;
    }
}
